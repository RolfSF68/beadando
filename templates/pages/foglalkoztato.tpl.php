        <p id="strongId"><strong>Fejlesztő foglalkoztatás</strong></p>
        <p class="bekezdes"> A munka, az értelmes és hasznos tevékenység nélkülözhetetlen a mi ellátottaink életében is – a vele járó fáradtság
            mellett illetve ezzel együtt – sikerélményt nyújt, önbizalmat erősít, én, és öntudatot növel.</p>
        <p class="bekezdes">Munkatevékenység alatt a következőket értjük: szövőanyag előkészítése (hurkolás, gombolyítás, szövés, bedolgozás,
            zokni ki, befordítás).</p>
        <p class="bekezdes">Intézményünk a szociális foglalkoztatás keretén belül fejlesztő foglalkoztatást valósít meg.</p>
        <p class="bekezdes">A fejlesztő foglalkoztatásban, fejlesztési jogviszony keretében fejlesztési szerződés alapján végzi az Igénybevevő
            a fejlesztési tevékenységet, amelynek célja az egyén testi és szellemi képességeinek és munkavégzéssel összefüggő
            készségeinek helyreállítása, megőrzése, fejlesztése, felkészítése az önálló munkavégzésre. Intézményünk a fejlesztő
            foglalkoztatás keretében Szt. szerinti jogviszonyban történő foglakoztatást valósít meg.</p>
        <p class="bekezdes">A foglalkoztatásban segítők figyelembe veszik a foglalkoztatott fogyatékosságainak sajátosságát és a szükséges mértékű
            segítséget mindenkor megadják. A foglalkoztatásban résztvevő személy mindig a képességének megfelelő munkát végzi.
            A személyre szabottabb fejlesztés érdekében a munkafolyamatot több részre osztottuk. Alapul véve a meglévő
            tapasztalatokat. A céljaink megvalósításához biztosítjuk a megfelelő körülményeket, környezetet, személyi
            feltételeket (szakemberek) és a folyamatos, naprakész adminisztrációt.</p>
        <p class="bekezdes">Sajnos a mindennapi életben, különösen a halmozottan sérült és vak emberek esetében nincs megfelelő munkahely,
            ahol ismereteiket segítséggel alkalmazhatnák. Ezt a hiányt szeretnénk pótolni intézményünkben, majd ezt a
            komplex tudást érvényesíteni vagy védett munkahelyen, vagy a nyílt munkaerőpiacon.</p>
